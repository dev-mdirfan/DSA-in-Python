#include <bits/stdc++.h>
using namespace std;

int main()
{
    // Integer(4 bytes) Range: -2^31 to 2^31-1
    int x = 10;            // Integer data type
    // Float(4 bytes) Range: 3.4E-38 to 3.4E+38
    float y = 10.5;        // Floating point data type
    // Double(8 bytes) Range: 1.7E-308 to 1.7E+308
    double d = 10.5;       // Double data type
    // Character(1 byte) Range: -128 to 127
    char z = 'A';          // Character data type
    // Boolean(1 byte) Range: 0 to 1
    bool a = true;         // Boolean data type
    // String(1 byte per character) Range: 0 to 2^31-1
    string name = "Irfan"; // String data type
    cout << "Value of x is: " << x << endl;
    cout << "Value of y is: " << y << endl;
    cout << "Value of d is: " << d << endl;
    cout << "Value of z is: " << z << endl;
    cout << "Value of a is: " << a << endl;
    cout << "Value of name is: " << name << endl;
    return 0;
}

